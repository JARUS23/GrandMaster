package com.jarus.utils;

public class Location {

    private int x;
    private int y;

    public Location(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    // GETTER
    public int getX()
    {
        return x;
    }

    // GETTER
    public int getY()
    {
        return y;
    }
}
